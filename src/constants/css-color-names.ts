const hexVals: Record<string, string> = hex;

type ColorStrings = Record<string, { hex: string; rgb: string; hsl: string }>;

const strings = Object.entries(arrays).reduce((acc, [colorName, colorValues]) => {
	// Combine CSS color names with hex, RGB strings, and HSL strings
	// RGB to string
	function rgbArrayToString(rgb: readonly [number, number, number]): string {
		return `rgb(${rgb[0]}, ${rgb[1]}, ${rgb[2]})`;
	}

	
	// HSL to string
	function hslArrayToString(hsl: readonly [number, number, number]): string {
		return `hsl(${hsl[0]}, ${hsl[1]}%, ${hsl[2]}%)`;
	}
  const hex = String(hexVals[colorName]);
  const rgb = rgbArrayToString(colorValues.rgb as [number, number, number]);
  const hsl = hslArrayToString(colorValues.hsl as [number, number, number]);
  acc[colorName] = { hex, rgb, hsl };
  return acc;
}, {} as ColorStrings);


